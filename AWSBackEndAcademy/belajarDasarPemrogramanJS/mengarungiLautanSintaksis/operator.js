/*

Operator adalah simbol atau teks yang digunakan untuk melakukan sebuah operasi, seperti:
-> aritmatika
-> penugasan
-> logika
-> tipe data
-> ...

*/

/*

Operator dalam javascript terbagi menjadi 3 kelompok:
1. unary (operator yang hanya memerlukan satu buah operan)
2. binary (operator yang memerlukan dua buah operan)
3. ternary (operator yang memerlukan tiga buah operan)

*/

// Contoh:
let age = 25;

// 1. Operator Unary
typeof age; // hanya memiliki 1 operan (age)

// 2. Operator Binary
5 + 4; // memiliki 2 operan (5 dan 4)
10 / 2; // memiliki 2 operan (10 dan 2)
age = 30; // memiliki 2 operan (age dan 30)

// 3. Operator Ternary
age < 18 ? "You are too young!" : "Welcome onboard!"; // memiliki 3 operan (age, 10 dan antara you are too young dengan welcome onboard!)

/*

Berbagai macam operator yang tersedia di JavaScript:

1. Assignment: operator yang digunakan untuk memberikan nilai kepada sebuah variabel, baik inisiasi nilai baru maupun mengubah nilai yang sudah ada.
2. Arithmetic: operator standar yang digunakan untuk proses aritmetika, seperti penambahan (+), pengurangan (-), pengalian (*), ataupun pembagian (/).
3. Comparison: operator yang digunakan untuk membandingkan dua nilai dan mengembalikan nilai boolean (true atau false) sebagai hasil perbandingan.
4. Logical: menetapkan logika dari dua nilai operan boolean.
5. String operator: operator yang digunakan untuk menggabungkan dua nilai string.

*/
