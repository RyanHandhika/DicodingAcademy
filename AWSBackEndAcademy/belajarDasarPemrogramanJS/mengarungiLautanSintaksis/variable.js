/*
=> Variabel adalah sebuah wadah untuk menampung sebuah nilai
=> Nilai yang ditampung dapat berupa angka, teks, atau apapun yang menghasilkan nilai (expression)
=> Variabel dapat di buat dengan 3 keyword dibawah ini:
=> 1. var (nilai nya dapat diubah)
=> 2. let (nilai nya dapat diubah)
=> 3. const (nilai nya tidak dapat diubah)
*/

// Contoh:
var nama = "Ryan Handhika";
let age = 19;
const JENISKELAMIN = "Pria";
