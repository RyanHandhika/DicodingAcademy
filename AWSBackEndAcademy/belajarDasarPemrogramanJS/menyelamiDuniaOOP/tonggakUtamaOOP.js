/*

=> Terdapat 4 Tonggak utama pada OOP, yaitu:
1. inheritance
2. encapsulation
3. polymorphism
4. abstraction

*/

/*

=> Inheritance
-> inheritance memungkinkan class untuk mewarisi property dan method yang dimilikinya,
-> sehingga membantu mengurangi penulisan kode secara berulang (mengurangi redundancy kode)

*/
// Contoh penulisan:
class SuperClass {} // SuperClass, Induk, Base, atau Parent Class
class SubClass extends SuperClass {} // SubClass dan Children Class (Anak).

/*

=> Encapsulation
-> proses untuk membungkus data di suatu wadah yang disebut dengan class.
-> Menyembunyikan data adalah bagian kunci dari encapsulation.
-> Desain OOP yang baik adalah object hanya akan menampilkan data yang dibutuhkan oleh object lain.
-> Data akan diisolasi dan tidak dapat diakses langsung dari luar.
-> Secara sederhana, encapsulation adalah membuat data yang ada di class sebagai private.

*/

/*

=> Polymorphism
-> konsep di mana suatu entitas menjadi SuperClass untuk mewariskan property atau method ke SubClass.

*/
